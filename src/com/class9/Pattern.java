package com.class9;

public class Pattern {

public static void main(String[] args) {
	for(int i=0; i<=4; i++) {
		for(int j=1; j<5; j++) {
			System.out.print("*");
		}
		System.out.println();
	}
	for(int i=1; i<=6; i++) {
		for(int y=1; y<=10; y++) {
			System.out.print("+");
		}
		System.out.println();
	}
	for(int i=1; i<5; i++) {
		for(int j=1; j<6; j++) {
			System.out.print(j);
		}
		System.out.println();
	}
	for(int i=1; i<5; i++) {
		for(int j=1; j<6; j++) {
			System.out.print(i);
		}
		System.out.println();
	}
	}
}
	

 