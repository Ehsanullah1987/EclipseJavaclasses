package com.class7;

public class ForLoopTask1 {
	public static void main(String[] args) {
		for(int i=1; i<=100; i++) {
			System.out.print(" " +i+" ");
		}
		for(int i=100; i>=1; i--) {
			System.out.print(" " +i+" ");
		}
		for(int even=20; even>=1; even-=2) {
			System.out.println(even);
		}
	for(int odd=20; odd<=50; odd++) {
		System.out.println(odd);
	}
	for (int i=20; i<=50; i++) {
		if(i%2==0)
			System.out.println(i);
	}
	}

}
